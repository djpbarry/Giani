    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        componentList.get(layerIndex).setVariables();
        img = componentList.get(layerIndex).getImg();
        props.putAll(componentList.get(layerIndex).getPanelProps());
        layerIndex++;
        updateLayer();
        checkLayerIndex();
        componentList.get(layerIndex).setImg(img);
        componentList.get(layerIndex).setInputProps(props);
    }//GEN-LAST:event_nextButtonActionPerformed

    private void previousButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_previousButtonActionPerformed
        Properties inputProps = null;
        componentList.get(layerIndex).setVariables();
        layerIndex--;
        if (layerIndex > 0) {
            img = componentList.get(layerIndex - 1).getImg();
            inputProps = componentList.get(layerIndex - 1).getPanelProps();
        }
        updateLayer();
        checkLayerIndex();
        componentList.get(layerIndex).setImg(img);
        componentList.get(layerIndex).setInputProps(inputProps);
    }//GEN-LAST:event_previousButtonActionPerformed

    private void saveParamsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveParamsButtonActionPerformed
        setVariables();
        try {
            PropertyWriter.printProperties(props, props.getProperty(SelectInputPanel.INPUT_DIR_LABEL), title, true);
        } catch (Exception e) {
            GenUtils.error("Failed to save property file.");
            GenUtils.logError(e);
        }
        cleanUp();
    }//GEN-LAST:event_saveParamsButtonActionPerformed

    void updateLayer() {
        for (int i = 0; i < componentList.size(); i++) {
            if (i == layerIndex) {
                componentList.get(i).setVisible(true);
            } else {
                componentList.get(i).setVisible(false);
            }
        }
        componentList.get(layerIndex).setImg(img);
        if (img != null) {
            statusTextArea.append(img.toString());
        }
    }

    void checkLayerIndex() {
        if (layerIndex == 0) {
            previousButton.setEnabled(false);
        } else {
            previousButton.setEnabled(true);
        }
        if (layerIndex == componentList.size() - 1) {
            nextButton.setEnabled(false);
        } else {
            nextButton.setEnabled(true);
        }
    }

